let month = +prompt("İlin ayını rəqəm olaraq daxil edin")

if (month == 1) {
  document.write("Yanvar")
}
else if (month == 2) {
  document.write("Fevral")
}
else if (month == 3) {
  document.write("Mart")
}
else if (month == 4) {
  document.write("Aprel")
}
else if (month == 5) {
  document.write("May")
}
else if (month == 6) {
  document.write("İyun")
}
else if (month == 7) {
  document.write("İyul")
}
else if (month == 8) {
  document.write("Avqust")
}
else if (month == 9) {
  document.write("Sentyabr")
}
else if (month == 10) {
  document.write("Oktyabr")
}
else if (month == 11) {
  document.write("Noyabr")
}
else if (month == 12) {
  document.write("Dekabr")
}
else if (month > 12 || month < 1) {
  alert("Daxil etdiyiniz dəyər yanlışdır")
}
else {
  alert("Daxil etdiyiniz dəyər rəqəm deyil")
}




